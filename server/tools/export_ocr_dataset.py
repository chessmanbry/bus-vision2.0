"""Export BusVision annotations into an OCR-friendly dataset.

Input:
  server/data/index.jsonl  (generated by backend)

Output:
  server/data/ocr_dataset/images/<id>.jpg   (cropped by annotation box)
  server/data/ocr_dataset/labels.csv        (filename,label)

Assumptions:
  - annotation meta includes: meta.annotation.box {x,y,w,h}
  - annotation meta includes: meta.annotation.canvasWidth/canvasHeight (optional)
    If absent, crop uses pixel coords as-is.

Usage:
  python export_ocr_dataset.py

Note:
  You need Pillow:  pip install pillow
"""

from __future__ import annotations

import csv
import json
import os
from pathlib import Path

from PIL import Image


ROOT = Path(__file__).resolve().parents[1]
DATA_DIR = ROOT / "data"
INDEX_FILE = DATA_DIR / "index.jsonl"

OUT_DIR = DATA_DIR / "ocr_dataset"
IMG_DIR = OUT_DIR / "images"
LABELS_CSV = OUT_DIR / "labels.csv"


def clamp(v: float, lo: float, hi: float) -> float:
    return max(lo, min(hi, v))


def main() -> None:
    if not INDEX_FILE.exists():
        raise SystemExit(f"index not found: {INDEX_FILE}")

    IMG_DIR.mkdir(parents=True, exist_ok=True)

    rows: list[tuple[str, str]] = []

    with INDEX_FILE.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            if rec.get("type") != "annotation":
                continue

            file_rel = rec.get("file")
            meta = rec.get("meta") or {}
            ann = meta.get("annotation") or {}
            box = ann.get("box") or {}
            label = (ann.get("label") or meta.get("videoLabel") or "").strip()
            if not label:
                continue

            # backend stores file as path relative to DATA_DIR
            img_path = DATA_DIR / file_rel
            if not img_path.exists():
                continue

            try:
                x = float(box.get("x", 0))
                y = float(box.get("y", 0))
                w = float(box.get("w", 0))
                h = float(box.get("h", 0))
            except Exception:
                continue

            if w <= 1 or h <= 1:
                continue

            with Image.open(img_path) as im:
                # Some images may be rotated; we keep as-is.
                W, H = im.size

                # Clamp crop to image bounds.
                left = int(clamp(x, 0, W - 1))
                top = int(clamp(y, 0, H - 1))
                right = int(clamp(x + w, 1, W))
                bottom = int(clamp(y + h, 1, H))

                if right - left < 2 or bottom - top < 2:
                    continue

                crop = im.crop((left, top, right, bottom)).convert("RGB")

                out_name = f"{ann.get('id', '') or rec.get('receivedAt', '')}_{label}.jpg"
                out_name = "".join(ch if ch.isalnum() or ch in "._-" else "_" for ch in out_name)
                out_path = IMG_DIR / out_name
                crop.save(out_path, quality=92)

                rows.append((out_name, label))

    with LABELS_CSV.open("w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(["filename", "label"])
        w.writerows(rows)

    print(f"Exported {len(rows)} crops")
    print(f"Images:  {IMG_DIR}")
    print(f"Labels:  {LABELS_CSV}")


if __name__ == "__main__":
    main()
